// دالة تنسيق الاسم (زخرفة)
function formatName(name) {
    return `★ *${name}* ★`;
}

async function fadeyaCommand(sock, chatId, message) {
    const fullText = message.message?.conversation || 
                     message.message?.extendedTextMessage?.text || 
                     '';
    
    const parts = fullText.trim().split(/\s+/);
    const action = parts[1] ? parts[1].toLowerCase() : null;

    console.log(`[فعالية] النص الكامل: "${fullText}"`);
    console.log(`[فعالية] الإجراء: "${action}"`);

    // إيقاف الإرسال
    if (action === 'ايقاف' || action === 'off' || action === 'stop') {
        if (timers.has(chatId)) {
            clearInterval(timers.get(chatId));
            timers.delete(chatId);
            await sock.sendMessage(chatId, { text: '⏹️ تم إيقاف إرسال الأسماء بنجاح.' }, { quoted: message });
        } else {
            await sock.sendMessage(chatId, { text: '⚠️ لا يوجد إرسال نشط في هذه المجموعة.' }, { quoted: message });
        }
        return;
    }

    // إذا كان هناك مؤقت نشط، أوقفه (لإعادة التشغيل)
    if (timers.has(chatId)) {
        clearInterval(timers.get(chatId));
        timers.delete(chatId);
    }

    // بدء الإرسال (كل 2 ثانية)
    await sock.sendMessage(chatId, { text: '🎬 بدأ إرسال أسماء شخصيات الأنمي كل 2 ثانية.\nلإيقافه اكتب: `.فعالية ايقاف`' }, { quoted: message });

    const intervalId = setInterval(async () => {
        const randomName = ANIME_NAMES[Math.floor(Math.random() * ANIME_NAMES.length)];
        const formattedName = formatName(randomName);
        try {
            await sock.sendMessage(chatId, { text: formattedName });
        } catch (err) {
            console.error('خطأ في إرسال اسم أنمي:', err);
            if (timers.has(chatId)) {
                clearInterval(timers.get(chatId));
                timers.delete(chatId);
            }
        }
    }, 2000); // 2 ثانية

    timers.set(chatId, intervalId);
}

module.exports = fadeyaCommand;